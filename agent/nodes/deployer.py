async def _spawn_background_process(command: list[str], cwd: Path) -> None:
    try:
        await asyncio.create_subprocess_exec(
            *command,
            cwd=str(cwd),
            stdout=asyncio.subprocess.DEVNULL,
            stderr=asyncio.subprocess.DEVNULL,
            start_new_session=True,
        )
    except FileNotFoundError:
        logger.warning("[DEPLOYER] Binary not found: %s", command[0])


async def _run_foreground_command(command: list[str], cwd: Path) -> bool:
    try:
        process = await asyncio.create_subprocess_exec(
            *command,
            cwd=str(cwd),
            stdout=asyncio.subprocess.PIPE,
            stderr=asyncio.subprocess.PIPE,
        )
    except FileNotFoundError:
        logger.warning("[DEPLOYER] Binary not found: %s", command[0])
        return False

    stdout, stderr = await process.communicate()
    if process.returncode != 0:
        output = (stderr or stdout or b"").decode("utf-8", errors="replace")[-500:]
        logger.warning("[DEPLOYER] Command failed (%s): %s", " ".join(command), output)
        return False
    return True


async def _wait_for_http_service(url: str, *, require_success: bool, timeout_seconds: float = 45.0) -> bool:
    deadline = time.monotonic() + timeout_seconds
    while time.monotonic() < deadline:
        try:
            req = request.Request(url, method="GET")
            with request.urlopen(req, timeout=3) as response:
                if require_success:
                    if 200 <= response.status < 400:
                        return True
                else:
                    return True
        except error.HTTPError as exc:
            if not require_success and exc.code < 500:
                return True
        except (error.URLError, TimeoutError, OSError):
            pass
        await asyncio.sleep(1)
    return False


def _find_free_port(start_port: int) -> int:
    for port in range(start_port, start_port + 200):
        with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
            sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
            if sock.connect_ex(("127.0.0.1", port)) != 0:
                return port
    return start_port


