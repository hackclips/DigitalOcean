def _use_local_deploy_mode() -> bool:
    return os.getenv("VIBEDEPLOY_DEPLOY_MODE", "").strip().lower() in {"local", "offline"}


async def _emit_runtime_event(
    event_name: str,
    *,
    config,
    node: str,
    phase: str,
    message: str,
    **extra,
) -> None:
    if config is None:
        return
    payload = {
        "type": event_name,
        "node": node,
        "stage": node,
        "phase": phase,
        "message": message,
        **extra,
    }
    await adispatch_custom_event(event_name, payload, config=config)


async def _emit_step_start(node: str, message: str, *, config, **extra) -> None:
    await _emit_runtime_event("deploy.step.start", config=config, node=node, phase=node, message=message, **extra)


async def _emit_step_complete(node: str, message: str, *, config, **extra) -> None:
    await _emit_runtime_event("deploy.step.complete", config=config, node=node, phase=node, message=message, **extra)


async def _emit_step_error(node: str, message: str, *, config, **extra) -> None:
    await _emit_runtime_event("deploy.step.error", config=config, node=node, phase=node, message=message, **extra)


async def deployer(state: VibeDeployState, config=None) -> dict:
    frontend_code = state.get("frontend_code", {}) or {}
    backend_code = state.get("backend_code", {}) or {}
    blueprint = state.get("blueprint", {}) or {}
    code_eval_result = state.get("code_eval_result", {}) or {}
    idea = state.get("idea", {}) or {}
    selected_flagship = str(state.get("selected_flagship") or idea.get("selected_flagship") or "").strip()

    build_validation = state.get("build_validation") or {}
    deploy_gate_result = state.get("deploy_gate_result") or {}
    app_name = str(blueprint.get("app_name") or "").strip() or _build_repo_name(idea)
    description = idea.get("tagline") or idea.get("problem") or "Generated by vibeDeploy"
    deploy_blocker = _get_deploy_blocker(
        frontend_code,
        backend_code,
        blueprint,
        code_eval_result,
        selected_flagship,
        build_validation,
        deploy_gate_result,
    )
    if deploy_blocker:
        logger.error("[DEPLOYER] Generated code incomplete; skipping deployment: %s", deploy_blocker)
        await _emit_step_error(
            "git_push", f"Skipping deployment: {deploy_blocker}", config=config, error=deploy_blocker
        )
        return {
            "deploy_result": {
                "app_id": "",
                "live_url": "",
                "github_repo": "",
                "status": f"code_incomplete: {deploy_blocker}",
            },
            "phase": "deployment_skipped",
            "error": deploy_blocker,
        }

    all_files = _merge_files(frontend_code, backend_code)
    has_frontend = any(k.startswith("web/") for k in all_files)

    frontend_file_count = len(frontend_code)
    backend_file_count = len(backend_code)
    logger.info(
        "[DEPLOYER] File counts — frontend: %d, backend: %d, merged: %d, has_frontend: %s",
        frontend_file_count,
        backend_file_count,
        len(all_files),
        has_frontend,
    )

    if not has_frontend and backend_file_count > 0:
        logger.warning(
            "[DEPLOYER] Frontend code is empty — deploying backend-only. "
            "Ensuring backend has a landing page at root URL."
        )
        if "main.py" in all_files and '@app.get("/")' not in all_files.get("main.py", ""):
            all_files["main.py"] = _inject_landing_page(all_files["main.py"], idea)
            logger.info("[DEPLOYER] Injected HTML landing page into main.py")

    all_files = _ensure_unique_backend_table_prefix(all_files, app_name)
    all_files = await _prepare_files_for_push(all_files)

    all_files.update(
        {
            "README.md": _generate_readme(idea, state.get("scoring") or {}, backend_code),
            ".github/workflows/ci.yml": _generate_ci_yml(has_frontend=has_frontend),
            ".env.example": _generate_env_example(),
        }
    )

    if _use_local_deploy_mode():
        await _emit_step_complete("git_push", "Local deploy mode enabled: skipping GitHub/DO deploy", config=config)
        result = await _local_fallback_deploy(app_name, all_files, "", "local_mode")
        result["deploy_result"].update({"ci_status": "skipped", "ci_url": "", "ci_repair_attempts": 0})
        return result

    github_org = os.getenv("GITHUB_ORG")
    await _emit_step_start("git_push", "Creating repository and pushing generated files", config=config)

    github_result = await create_github_repo(
        name=app_name,
        files=all_files,
        description=description,
        org=github_org or None,
    )

    github_repo_url = github_result.get("url", "")
    github_clone_url = github_result.get("clone_url", "")
    github_full_name = github_result.get("full_name", "")

    if github_result.get("status") == "error" or not github_clone_url:
        error_message = github_result.get("error", "GitHub repository creation failed")
        await _emit_step_error("git_push", f"GitHub push failed: {error_message}", config=config, error=error_message)
        if "GITHUB_TOKEN not set" in error_message:
            result = await _local_fallback_deploy(app_name, all_files, github_repo_url, "github_token_missing")
            return result
        return {
            "deploy_result": {
                "app_id": "",
                "live_url": "",
                "github_repo": github_repo_url,
                "status": f"github_error: {error_message}",
            },
            "phase": "deployed",
        }

    supporting_files = {
        "README.md": _generate_readme(idea, state.get("scoring") or {}, backend_code),
        ".github/workflows/ci.yml": _generate_ci_yml(has_frontend=has_frontend),
        ".env.example": _generate_env_example(),
    }
    commit_sha = ""
    if github_full_name:
        push_result = await push_files(
            {"full_name": github_full_name},
            supporting_files,
            commit_message="Add project docs, CI workflow, and env example",
        )
        commit_sha = push_result.get("commit_sha", "")
    await _emit_step_complete(
        "git_push",
        "Repository push complete" if commit_sha else "Repository created; support commit metadata unavailable",
        config=config,
        github_repo=github_repo_url,
        commit_sha=commit_sha,
    )

    ci_result = {"status": "skipped"}
    repair_attempts = 0
    if github_full_name and commit_sha:
        await _emit_step_start("ci_test", "Running CI workflow", config=config, commit_sha=commit_sha)
        ci_result, all_files, repair_attempts = await _ci_repair_loop(
            github_full_name, commit_sha, all_files, max_retries=3, config=config
        )
        ci_status = ci_result.get("status", "skipped")
        if ci_status in {"passed", "skipped"}:
            await _emit_step_complete(
                "ci_test",
                "CI passed" if ci_status == "passed" else "CI skipped",
                config=config,
                ci_status=ci_status,
                ci_url=ci_result.get("url", ""),
                ci_repair_attempts=repair_attempts,
            )
        else:
            await _emit_step_error(
                "ci_test",
                "CI failed" if ci_status == "failed" else "CI timed out",
                config=config,
                ci_status=ci_status,
                ci_url=ci_result.get("url", ""),
                ci_repair_attempts=repair_attempts,
            )
    else:
        await _emit_step_complete(
            "ci_test",
            "CI skipped: no workflow commit available",
            config=config,
            ci_status="skipped",
        )

    ci_meta = {
        "ci_status": ci_result.get("status", "skipped"),
        "ci_url": ci_result.get("url", ""),
        "ci_repair_attempts": repair_attempts,
    }

    do_token = os.getenv("DIGITALOCEAN_API_TOKEN")
    if not do_token:
        if github_full_name:
            _set_repo_metadata(
                github_full_name,
                topics=["ai", "fastapi", "digitalocean", "vibedeploy", "hackathon", "llm"],
                homepage=github_repo_url,
            )
        await _emit_step_error("app_spec", "App Spec skipped: DigitalOcean token missing", config=config)
        result = await _local_fallback_deploy(app_name, all_files, github_repo_url, "do_token_missing")
        result["deploy_result"].update(ci_meta)
        return result

    await _emit_step_start("app_spec", "Generating App Platform specification", config=config)
    app_spec = build_app_spec(app_name, github_clone_url, has_frontend=has_frontend)
    await _emit_step_complete(
        "app_spec",
        "App Platform specification ready",
        config=config,
        has_frontend=has_frontend,
    )

    deploy_progress = {
        "build_started": False,
        "build_completed": False,
        "deploy_started": False,
        "deploy_completed": False,
    }

    async def _track_do_phase(phase_name: str) -> None:
        normalized = str(phase_name or "").upper()
        if "BUILD" in normalized:
            if not deploy_progress["build_started"]:
                deploy_progress["build_started"] = True
                await _emit_step_start("do_build", "DigitalOcean build started", config=config, do_phase=phase_name)
            return

        if "DEPLOY" in normalized:
            if not deploy_progress["build_started"]:
                deploy_progress["build_started"] = True
                await _emit_step_start("do_build", "DigitalOcean build started", config=config, do_phase=phase_name)
            if not deploy_progress["build_completed"]:
                deploy_progress["build_completed"] = True
                await _emit_step_complete("do_build", "DigitalOcean build complete", config=config, do_phase=phase_name)
            if not deploy_progress["deploy_started"]:
                deploy_progress["deploy_started"] = True
                await _emit_step_start("do_deploy", "DigitalOcean deploy started", config=config, do_phase=phase_name)
            return

        if normalized == "ACTIVE":
            if not deploy_progress["build_started"]:
                deploy_progress["build_started"] = True
                await _emit_step_start("do_build", "DigitalOcean build started", config=config, do_phase=phase_name)
            if not deploy_progress["build_completed"]:
                deploy_progress["build_completed"] = True
                await _emit_step_complete("do_build", "DigitalOcean build complete", config=config, do_phase=phase_name)
            if not deploy_progress["deploy_started"]:
                deploy_progress["deploy_started"] = True
                await _emit_step_start("do_deploy", "DigitalOcean deploy started", config=config, do_phase=phase_name)
            if not deploy_progress["deploy_completed"]:
                deploy_progress["deploy_completed"] = True
                await _emit_step_complete(
                    "do_deploy", "DigitalOcean deploy complete", config=config, do_phase=phase_name
                )

    await _emit_step_start("do_build", "Submitting App Platform build", config=config)
    deploy_progress["build_started"] = True
    deploy_result = await _deploy_with_capacity_retry(app_name, github_clone_url, app_spec)

    if deploy_result.get("status") == "error":
        logger.error(
            "[DEPLOYER] DO app creation failed: %s",
            deploy_result.get("error", "unknown"),
        )
        await _emit_step_error(
            "do_build",
            f"App Platform build submission failed: {deploy_result.get('error', 'unknown')}",
            config=config,
            error=deploy_result.get("error", "unknown"),
        )
        result = await _local_fallback_deploy(
            app_name,
            all_files,
            github_repo_url,
            f"do_deploy_failed: {deploy_result.get('error', 'DigitalOcean deployment failed')}",
            app_id=deploy_result.get("app_id", ""),
        )
        result["deploy_result"].update(ci_meta)
        return result

    app_id = deploy_result.get("app_id", "")
    live_url = ""
    deploy_repair_attempts = 0

    for attempt in range(3):
        live_url = await wait_for_deployment(app_id, on_phase_change=_track_do_phase) if app_id else ""
        if live_url:
            break

        await asyncio.sleep(15)
        error_logs = await get_deploy_error_logs(app_id)
        if not error_logs:
            logger.warning("[DEPLOYER] Deploy failed but no error logs available (attempt %d)", attempt + 1)
            break

        logger.warning("[DEPLOYER] Deploy attempt %d failed. Repairing from error logs.", attempt + 1)
        await _emit_step_start(
            "do_build",
            f"Deploy repair attempt {attempt + 1}/3",
            config=config,
            deploy_repair_attempt=attempt + 1,
        )

        fixed_files = await _repair_code_from_errors(all_files, error_logs)
        if fixed_files == all_files:
            logger.warning("[DEPLOYER] LLM could not fix deploy errors (attempt %d)", attempt + 1)
            break

        all_files = await _prepare_files_for_push(fixed_files)
        push_result = await push_files(
            {"full_name": github_full_name},
            all_files,
            commit_message=f"fix: deploy repair attempt {attempt + 1}",
        )
        if not push_result.get("commit_sha"):
            logger.warning("[DEPLOYER] Failed to push repair (attempt %d)", attempt + 1)
            break

        await asyncio.sleep(5)
        redeploy_result = await redeploy_app(app_id)
        if redeploy_result.get("status") == "error":
            logger.warning("[DEPLOYER] Redeploy trigger failed: %s", redeploy_result.get("error", ""))
            break

        await _emit_step_complete(
            "do_build",
            f"Deploy repair attempt {attempt + 1} — redeploying",
            config=config,
            deploy_repair_attempt=attempt + 1,
        )

        deploy_repair_attempts = attempt + 1

    ci_meta["deploy_repair_attempts"] = deploy_repair_attempts

    if not live_url:
        failed_node = (
            "do_deploy" if deploy_progress["deploy_started"] or deploy_progress["build_completed"] else "do_build"
        )
        await _emit_step_error(
            failed_node,
            "DigitalOcean did not produce a live URL",
            config=config,
            app_id=app_id,
            deploy_repair_attempts=deploy_repair_attempts,
        )
        result = await _local_fallback_deploy(
            app_name,
            all_files,
            github_repo_url,
            f"do_deploy_failed_after_{deploy_repair_attempts}_repairs",
            app_id=app_id,
        )
        result["deploy_result"].update(ci_meta)
        return result

    if not deploy_progress["build_completed"]:
        deploy_progress["build_completed"] = True
        await _emit_step_complete("do_build", "DigitalOcean build complete", config=config, app_id=app_id)
    if not deploy_progress["deploy_started"]:
        deploy_progress["deploy_started"] = True
        await _emit_step_start("do_deploy", "DigitalOcean deploy started", config=config, app_id=app_id)
    if not deploy_progress["deploy_completed"]:
        deploy_progress["deploy_completed"] = True
        await _emit_step_complete(
            "do_deploy", "DigitalOcean deploy complete", config=config, app_id=app_id, live_url=live_url
        )

    await _emit_step_start("verified", "Verifying live URL", config=config, live_url=live_url)
    url_verification = _verify_live_url(live_url)
    v_score = url_verification.get("verification_score", 0)
    v_verdict = url_verification.get("verification_verdict", "failed")

    if v_verdict == "verified":
        logger.info(
            "[DEPLOYER] Live URL verified (score=%d, verdict=%s): %s",
            v_score, v_verdict, live_url,
        )
        await _emit_step_complete(
            "verified",
            f"Live URL verified (score={v_score}/100)",
            config=config,
            live_url=live_url,
            verification_score=v_score,
            verification_verdict=v_verdict,
        )
    elif v_verdict == "partial":
        logger.warning(
            "[DEPLOYER] Live URL partially verified (score=%d, verdict=%s): %s",
            v_score, v_verdict, live_url,
        )
        await _emit_step_complete(
            "verified",
            f"Live URL partially verified (score={v_score}/100) — basic functionality works, some issues detected",
            config=config,
            live_url=live_url,
            verification_score=v_score,
            verification_verdict=v_verdict,
        )
    else:
        # verdict == "failed" (score < 50) — attempt repair
        logger.warning(
            "[DEPLOYER] Live URL verification FAILED (score=%d, verdict=%s): %s — attempting repair",
            v_score, v_verdict, live_url,
        )
        await _emit_step_error(
            "verified",
            f"Live URL verification failed (score={v_score}/100) — attempting repair",
            config=config,
            live_url=live_url,
            verification_score=v_score,
            verification_verdict=v_verdict,
        )

        # Build a diagnostic summary from the verification result for the repair LLM
        diag_lines = [f"Verification score: {v_score}/100 (verdict: {v_verdict})"]
        if not url_verification.get("root_ok"):
            diag_lines.append("CRITICAL: Root page did not return 200 or returned <100 bytes")
        if not url_verification.get("health_ok"):
            diag_lines.append("CRITICAL: /health endpoint did not return 200")
        cs = url_verification.get("content_signals", {})
        if not cs.get("has_title"):
            diag_lines.append("Missing: <title> tag in HTML")
        if not cs.get("has_semantic_html"):
            diag_lines.append("Missing: semantic HTML elements (h1, nav, main)")
        if not cs.get("has_interactive_elements"):
            diag_lines.append(f"Missing: interactive elements (only {cs.get('interactive_count', 0)} found, need >=3)")
        if cs.get("broken_images"):
            diag_lines.append(f"Broken images: {cs['broken_images'][:3]}")
        failed_eps = [
            k for k, v in url_verification.get("endpoint_results", {}).items()
            if not v.get("ok")
        ]
        if failed_eps:
            diag_lines.append(f"Failed API endpoints: {', '.join(failed_eps[:5])}")
        failed_assets = [
            k for k, v in url_verification.get("asset_checks", {}).items()
            if not v.get("ok")
        ]
        if failed_assets:
            diag_lines.append(f"Failed assets: {', '.join(failed_assets[:5])}")

        diag_summary = "\n".join(diag_lines)
        logger.info("[DEPLOYER] Verification diagnostics for repair:\n%s", diag_summary)

        # Attempt one repair cycle using the verification diagnostics
        error_logs = await get_deploy_error_logs(app_id) if app_id else ""
        combined_errors = f"=== Live URL Verification Failures ===\n{diag_summary}"
        if error_logs:
            combined_errors += f"\n\n=== Deploy Error Logs ===\n{error_logs}"

        fixed_files = await _repair_code_from_errors(all_files, combined_errors)
        if fixed_files != all_files:
            all_files = await _prepare_files_for_push(fixed_files)
            push_result = await push_files(
                {"full_name": github_full_name},
                all_files,
                commit_message="fix: repair from live URL verification failures",
            )
            if push_result.get("commit_sha") and app_id:
                await asyncio.sleep(5)
                redeploy_result = await redeploy_app(app_id)
                if redeploy_result.get("status") != "error":
                    repair_url = await wait_for_deployment(app_id)
                    if repair_url:
                        live_url = repair_url
                        # Re-verify after repair
                        url_verification = _verify_live_url(live_url)
                        v_score = url_verification.get("verification_score", 0)
                        v_verdict = url_verification.get("verification_verdict", "failed")
                        logger.info(
                            "[DEPLOYER] Post-repair verification: score=%d, verdict=%s",
                            v_score, v_verdict,
                        )

    homepage = live_url or github_repo_url
    if github_full_name:
        _set_repo_metadata(
            github_full_name,
            topics=["ai", "fastapi", "digitalocean", "vibedeploy", "hackathon", "llm"],
            homepage=homepage,
        )

    if live_url and github_full_name:
        updated_readme = _generate_readme(idea, state.get("scoring") or {}, backend_code, live_url=live_url)
        try:
            await push_files(
                {"full_name": github_full_name},
                {"README.md": updated_readme},
                commit_message="docs: add live demo URL to README",
            )
            logger.info("[DEPLOYER] README updated with live URL: %s", live_url)
        except Exception:
            logger.warning("[DEPLOYER] Failed to update README with live URL")

    return {
        "deploy_result": {
            "app_id": app_id,
            "live_url": live_url,
            "github_repo": github_repo_url,
            "status": "deployed",
            "frontend_files": frontend_file_count,
            "backend_files": backend_file_count,
            "url_verification": url_verification,
            "verification_score": v_score,
            "verification_verdict": v_verdict,
            **ci_meta,
        },
        "phase": "deployed",
    }


async def _deploy_with_capacity_retry(app_name: str, github_clone_url: str, app_spec: dict) -> dict:
    deploy_result = await deploy_to_digitalocean(github_clone_url, app_spec)
    cleanup_attempts = 0

    while _is_do_app_limit_error(deploy_result) and cleanup_attempts < 3:
        cleanup_result = await _reclaim_do_app_capacity(app_name)
        if cleanup_result.get("status") != "deleted":
            break
        cleanup_attempts += 1
        deploy_result = await deploy_to_digitalocean(github_clone_url, app_spec)

    return deploy_result


def _is_do_app_limit_error(deploy_result: dict) -> bool:
    return (
        deploy_result.get("status") == "error" and _DO_APP_LIMIT_ERROR_SNIPPET in deploy_result.get("error", "").lower()
    )


async def _reclaim_do_app_capacity(target_app_name: str) -> dict:
    apps = await list_apps()
    failed_candidates = []
    live_candidates = []

    for app in apps:
        app_id = app.get("id", "")
        name = app.get("spec", {}).get("name", "")
        live_url = app.get("live_url", "")
        created_at = app.get("created_at", "")
        active_phase = app.get("active_deployment", {}).get("phase", "")
        in_progress_phase = app.get("in_progress_deployment", {}).get("phase", "")
        phase = in_progress_phase or active_phase or "UNKNOWN"

        if not app_id or not name or name in {target_app_name, "vibedeploy"}:
            continue
        if in_progress_phase or phase in {"PENDING", "BUILDING", "DEPLOYING"}:
            continue
        candidate = {
            "id": app_id,
            "name": name,
            "created_at": created_at,
            "phase": phase,
            "live_url": live_url,
        }
        if live_url:
            live_candidates.append(candidate)
        else:
            failed_candidates.append(candidate)

    candidates = failed_candidates or live_candidates
    if not candidates:
        logger.warning("[DEPLOYER] DO app limit reached but no generated apps were eligible for deletion")
        return {"status": "no_candidate"}

    candidates.sort(key=lambda app: (app.get("created_at", ""), app.get("name", "")))
    candidate = candidates[0]
    candidate_kind = "failed" if not candidate.get("live_url") else "oldest live"
    logger.warning(
        "[DEPLOYER] Deleting %s DO app to free capacity: %s (%s, phase=%s)",
        candidate_kind,
        candidate["name"],
        candidate["id"],
        candidate["phase"],
    )
    delete_result = await delete_app(candidate["id"])
    if delete_result.get("status") == "deleted":
        await asyncio.sleep(10)
    return delete_result


